//Passaremos por alguns atalhos de atribuição e operações aritmeticas
const a = 7;
let b = 3;

b += a; //Não estou substituindo B, estou somando o valor de A, mesma coisa que: b = b + a
console.log(b);

b -= 4; //Não estou substituindo B, estou substraindo de B o valor 4, mesma coisa que: b = b - 4
console.log(b);

b *= 2; //Não estou substituindo B, estou multiplicando o valor de B por 2, mesma coisa que: b = b * 2
console.log(b);

b /= 2; //Não estou substituindo B, estou dividindo o valor de B por 2 , mesma coisa que: b = b / 2
console.log(b);

b %= 2; //Não estou substituindo B, estou descobrindo o módulo(Resto) de B ao dividi-lo por 2 , mesma coisa que: b = b % 2
console.log(b);

